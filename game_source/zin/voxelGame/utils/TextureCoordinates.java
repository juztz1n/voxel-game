package zin.voxelGame.utils;

public class TextureCoordinates
{

	private float u, v;

	public TextureCoordinates(float u, float v)
	{
		this.u = u;
		this.v = v;
	}

	public float getU()
	{
		return u;
	}

	public void setU(float u)
	{
		this.u = u;
	}

	public float getV()
	{
		return v;
	}

	public void setV(float v)
	{
		this.v = v;
	}

}
